/* ============================================================
   TREINAMENTO
   ============================================================ */

CREATE TABLE rh.treinamento (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_treinamento varchar(150) NOT NULL,
    tipo_treinamento rh.tipo_treinamento_enum NOT NULL,
    obrigatorio boolean NOT NULL DEFAULT false,
    validade_meses integer,
    descricao text,
    ativo boolean NOT NULL DEFAULT true,

    CONSTRAINT ck_treinamento_validade_meses
        CHECK (
            validade_meses IS NULL 
            OR validade_meses >= 0
        )
);

CREATE TABLE rh.participacao_treinamento (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status rh.status_participacao_treinamento_enum NOT NULL,
    data_validade date,
    nota numeric(5,2),
    data_realizacao date NOT NULL,
    observacao text,

    colaborador_id bigint NOT NULL,
    treinamento_id bigint NOT NULL,

    CONSTRAINT ck_participacao_treinamento_nota
        CHECK (
            nota IS NULL 
            OR nota BETWEEN 0 AND 100
        ),

    CONSTRAINT uq_participacao_treinamento_colab_trein_data
        UNIQUE (colaborador_id, treinamento_id, data_realizacao),

    CONSTRAINT fk_participacao_treinamento_colaborador_participa
        FOREIGN KEY (colaborador_id)
        REFERENCES rh.colaborador(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_participacao_treinamento_treinamento_refere_se
        FOREIGN KEY (treinamento_id)
        REFERENCES rh.treinamento(id)
        ON DELETE RESTRICT
);


/* ============================================================
   RECRUTAMENTO
   ============================================================ */

CREATE TABLE rh.recrutamento_vaga (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo varchar(150) NOT NULL,
    descricao text,
    requisitos text,
    data_abertura date NOT NULL DEFAULT current_date,
    data_fechamento date,
    status rh.status_vaga_enum NOT NULL,
    quantidade_vagas integer NOT NULL,
    tipo_vinculo rh.tipo_vinculo_enum NOT NULL,
    salario_ofertado numeric(10,2),

    departamento_id bigint NOT NULL,
    turno_id bigint,

    CONSTRAINT ck_recrutamento_vaga_quantidade
        CHECK (quantidade_vagas > 0),

    CONSTRAINT ck_recrutamento_vaga_salario
        CHECK (
            salario_ofertado IS NULL 
            OR salario_ofertado >= 0
        ),

    CONSTRAINT ck_recrutamento_vaga_datas
        CHECK (
            data_fechamento IS NULL 
            OR data_fechamento >= data_abertura
        ),

    CONSTRAINT fk_recrutamento_vaga_departamento_oferta
        FOREIGN KEY (departamento_id)
        REFERENCES rh.departamento(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_recrutamento_vaga_turno_preve
        FOREIGN KEY (turno_id)
        REFERENCES rh.turno(id)
        ON DELETE RESTRICT
);

CREATE TABLE rh.candidato (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome varchar(150) NOT NULL,
    email varchar(150) NOT NULL,
    telefone varchar(20),
    data_cadastro date NOT NULL DEFAULT current_date,

    CONSTRAINT uq_candidato_email
        UNIQUE (email)
);

CREATE TABLE rh.candidatura (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data_candidatura date NOT NULL DEFAULT current_date,
    origem_candidatura rh.origem_candidatura_enum NOT NULL,
    status_candidatura rh.status_candidatura_enum NOT NULL,
    etapa_atual rh.etapa_candidatura_enum NOT NULL,
    observacao text,
    pretensao_salarial numeric(10,2),

    vaga_id bigint NOT NULL,
    candidato_id bigint NOT NULL,

    CONSTRAINT ck_candidatura_pretensao_salarial
        CHECK (
            pretensao_salarial IS NULL 
            OR pretensao_salarial >= 0
        ),

    CONSTRAINT uq_candidatura_vaga_candidato
        UNIQUE (vaga_id, candidato_id),

    CONSTRAINT fk_candidatura_recrutamento_vaga_recebe
        FOREIGN KEY (vaga_id)
        REFERENCES rh.recrutamento_vaga(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_candidatura_candidato_realiza
        FOREIGN KEY (candidato_id)
        REFERENCES rh.candidato(id)
        ON DELETE RESTRICT
);


