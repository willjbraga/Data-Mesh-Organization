/* ============================================================
   TABELAS BASE
   ============================================================ */

CREATE TABLE rh.unidade_restaurante (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_unidade varchar(100) NOT NULL,
    tipo_unidade rh.tipo_unidade_enum NOT NULL,
    cidade varchar(100) NOT NULL,
    estado char(2) NOT NULL,
    status rh.status_unidade_enum NOT NULL,

    CONSTRAINT ck_unidade_restaurante_estado
        CHECK (estado ~ '^[A-Z]{2}$')
);

CREATE TABLE rh.cargo (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_cargo varchar(100) NOT NULL,
    nivel varchar(50),
    descricao text,
    tipo_cargo rh.tipo_cargo_enum NOT NULL,
    ativo boolean NOT NULL DEFAULT true,
    salario_base numeric(10,2),
    faixa_salarial_min numeric(10,2),
    faixa_salarial_max numeric(10,2),

    CONSTRAINT ck_cargo_salario_base
        CHECK (salario_base IS NULL OR salario_base >= 0),

    CONSTRAINT ck_cargo_faixa_salarial_min
        CHECK (faixa_salarial_min IS NULL OR faixa_salarial_min >= 0),

    CONSTRAINT ck_cargo_faixa_salarial_max
        CHECK (faixa_salarial_max IS NULL OR faixa_salarial_max >= 0),

    CONSTRAINT ck_cargo_faixa_salarial_valida
        CHECK (
            faixa_salarial_min IS NULL
            OR faixa_salarial_max IS NULL
            OR faixa_salarial_min <= faixa_salarial_max
        )
);

CREATE TABLE rh.turno (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_turno varchar(80) NOT NULL,
    hora_inicio time NOT NULL,
    hora_fim time NOT NULL,
    descricao text,
    ativo boolean NOT NULL DEFAULT true,
    carga_horaria_semanal numeric(5,2),

    CONSTRAINT ck_turno_carga_horaria_semanal
        CHECK (
            carga_horaria_semanal IS NULL 
            OR carga_horaria_semanal > 0
        )
);

CREATE TABLE rh.departamento (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome_departamento varchar(100) NOT NULL,
    ativo boolean NOT NULL DEFAULT true,
    tipo_departamento rh.tipo_departamento_enum NOT NULL,
    descricao text,
    unidade_id bigint NOT NULL,

    CONSTRAINT fk_departamento_unidade_possui
        FOREIGN KEY (unidade_id)
        REFERENCES rh.unidade_restaurante(id)
        ON DELETE RESTRICT
);


/* ============================================================
   COLABORADOR
   ============================================================ */

CREATE TABLE rh.colaborador (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome varchar(150) NOT NULL,
    telefone varchar(20),
    data_admissao date NOT NULL,
    status rh.status_colaborador_enum NOT NULL,
    tipo_vinculo rh.tipo_vinculo_enum NOT NULL,
    email varchar(200),
    cpf char(11) NOT NULL,
    matricula varchar(30) NOT NULL,
    data_nascimento date,
    salario_atual numeric(10,2),
    data_desligamento date,
    observacao text,

    gerente_id bigint,
    cargo_id bigint NOT NULL,
    departamento_id bigint NOT NULL,
    turno_id bigint,

    CONSTRAINT uq_colaborador_cpf
        UNIQUE (cpf),

    CONSTRAINT uq_colaborador_matricula
        UNIQUE (matricula),

    CONSTRAINT uq_colaborador_email
        UNIQUE (email),

    CONSTRAINT ck_colaborador_cpf
        CHECK (cpf ~ '^[0-9]{11}$'),

    CONSTRAINT ck_colaborador_salario_atual
        CHECK (salario_atual IS NULL OR salario_atual >= 0),

    CONSTRAINT ck_colaborador_data_desligamento
        CHECK (
            data_desligamento IS NULL 
            OR data_desligamento >= data_admissao
        ),

    CONSTRAINT ck_colaborador_gerente_diferente
        CHECK (
            gerente_id IS NULL 
            OR gerente_id <> id
        ),

    CONSTRAINT fk_colaborador_colaborador_gerencia
        FOREIGN KEY (gerente_id)
        REFERENCES rh.colaborador(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_colaborador_cargo_ocupa
        FOREIGN KEY (cargo_id)
        REFERENCES rh.cargo(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_colaborador_departamento_alocado
        FOREIGN KEY (departamento_id)
        REFERENCES rh.departamento(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_colaborador_turno_cumpre
        FOREIGN KEY (turno_id)
        REFERENCES rh.turno(id)
        ON DELETE RESTRICT
);


/* ============================================================
   AUSÊNCIA
   ============================================================ */

CREATE TABLE rh.ausencia (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tipo_ausencia rh.tipo_ausencia_enum NOT NULL,
    status_aprovacao rh.status_aprovacao_enum NOT NULL,
    motivo text,
    observacao text,
    data_aprovacao date,
    data_solicitacao date NOT NULL DEFAULT current_date,
    data_inicio date NOT NULL,
    data_fim date NOT NULL,

    colaborador_id bigint NOT NULL,
    aprovador_id bigint,

    CONSTRAINT ck_ausencia_periodo
        CHECK (data_fim >= data_inicio),

    CONSTRAINT fk_ausencia_colaborador_registra
        FOREIGN KEY (colaborador_id)
        REFERENCES rh.colaborador(id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_ausencia_colaborador_aprova
        FOREIGN KEY (aprovador_id)
        REFERENCES rh.colaborador(id)
        ON DELETE RESTRICT
);


